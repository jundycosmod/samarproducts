<p align="justify"><strong>WESTERN SAMAR
</strong></p>
<p align="justify">	The province of Samar, occupying the southwestern part of Samar Island, is a rugged land with bountiful waters on the west and low mountain ranges on the east. Samar is blessed with a lush rainforest harboring tropical flora and fauna, crossed by multiple navigable rivers, pocked by mysterious caverns and refreshing waterfalls (wowphilippines.com).
  
  Inside the province, there are twenty (20) scenic spots, all having resorts with spring and sea beaches. 
</p>
<p align="justify">Samar has many raw materials which may be processed into a variety of commercial products: rattan for furniture, mangrove for wood, almaciga for boat coating, coconut palm leaves for baskets, and low-grade fish for fish meal. The province also has vast natural timberlands, from which a significant amount of the country &iuml;s&frac12;s wood exports originate.&nbsp; 
</p>
<p align="justify">One Town-One Product-Philippines (OTOP) is a program that create entrepreneurship and jobs. Western Samar is an active member of the program where it promotes the indigenous&nbsp; products of&nbsp; every town and its eco-tourism. 
</p>
<p align="justify">In this site every product produce in&nbsp; every town of Western Samar under the OTOP program and under the DTI Samar care&nbsp; is being transact to every consumer of which products you wishes to buy.
</p>
